    private void init() {
        this.setLocationRelativeTo(null);
        
        pnlThanhTieuDe.setVisible(false);
        this.lstModel = new DefaultListModel<>();

        this.formatTable();
        this.fillToComboBox();
        this.fillToList();
        this.updateStatus();
        
        timer.start();
    }

    // Đổ dữ liệu vào bảng.
    private void fillToTable() {
        tblModel.setRowCount(0);
        int maLHH = lstLHH.getSelectedValue().getMaLHH();
        String keyword = txtTimKiem.getText();
        int headerIndex = cboTimKiem.getSelectedIndex();
        int i = 1;
        try {
            List<HangHoa> list = hhDAO.selectByKeyword(maLHH, keyword, headerIndex);
            for (HangHoa hh : list) {
                Object[] row = {
                    i++,
                    hh.getMaHH(),
                    hh.getTenHH(),
                    hh.getDonGia(),
                    hh.getDonViTinh()
                };
                tblModel.addRow(row);
            }
            tblHangHoa.setModel(tblModel);
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
            e.printStackTrace();
        }
    }

    //Đổ dữ liệu vào List.
    private void fillToList() {
        lstModel.removeAllElements();
        try {
            List<LoaiHangHoa> list = lhhDAO.selectAll();
            for (LoaiHangHoa lhh : list) {
                lstModel.addElement(lhh);
            }
            lstLHH.setModel(lstModel);
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
            e.printStackTrace();
        }
    }

    // Đổ tên bảng vào ComboBox tìm kiếm
    private void fillToComboBox() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboTimKiem.getModel();
        model.removeAllElements();
        for (int i = 0; i < tblHangHoa.getColumnCount(); i++) {
            model.addElement(tblHangHoa.getColumnName(i));
        }
    }

    // Lấy dữ liệu từ form
    private HangHoa getForm() {
        HangHoa hh = new HangHoa();
        hh.setMaHH(txtMaHH.getText());
        hh.setTenHH(txtTenHH.getText().trim());
        hh.setDonViTinh(txtDonViTinh.getText());
        hh.setDonGia(XNumber.toDouble(txtDonGia.getText(), numPattern));
        hh.setMaLHH(lstLHH.getSelectedValue().getMaLHH());
        return hh;
    }

    // Làm mới form
    private void clearForm() {
        this.setForm(new HangHoa());
        this.row = -1;
        tblHangHoa.clearSelection();
        this.updateStatus();
    }

    // Đưa dữ liệu lên form
    private void setForm(HangHoa hh) {
        txtMaHH.setText(hh.getMaHH());
        txtTenHH.setText(hh.getTenHH());
        txtDonViTinh.setText(hh.getDonViTinh());
        txtDonGia.setText(XNumber.toString(hh.getDonGia(), numPattern));
    }

    //Code phương thức updateStatus.
    private void updateStatus() {
        boolean isSelectedList = !lstLHH.isSelectionEmpty();
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblHangHoa.getRowCount() - 1);
        boolean isManager = Auth.isManager();
        boolean isTableEmpty = (tblHangHoa.getRowCount() == 0);

        // Chỉ bật bộ sắp xếp khi bảng có dữ liệu
        tblHangHoa.setAutoCreateRowSorter(!isTableEmpty);
        // Chọn hàng trên bảng
        if (edit) {
            tblHangHoa.setRowSelectionInterval(row, row);
        }

        txtMaHH.setEditable(!edit && isSelectedList);
        btnThem.setEnabled(!edit && isSelectedList && isManager);
        btnSua.setEnabled(edit && isSelectedList && isManager);
        btnXoa.setEnabled(edit && isSelectedList && isManager);
        btnMoi.setEnabled(isSelectedList && isManager);
        btnThemList.setEnabled(isManager);
        btnXoaList.setEnabled(isManager);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    // Hiển thị dữ liệu hàng hoá được chọn trên bảng lên form
    private void edit() {
        String maHH = (String) tblHangHoa.getModel().getValueAt(this.row, 1);
        HangHoa hh = hhDAO.selectByID(maHH);
        this.setForm(hh);
        this.updateStatus();
    }

    // Hiển thị hàng hoá đầu danh sách bảng
    private void first() {
        this.row = 0;
        this.edit();
    }

    // Hiển thị hàng hoá kế trước
    private void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    // Hiển thị hàng hoá tiếp theo
    private void next() {
        if (this.row < (tblHangHoa.getRowCount() - 1)) {
            this.row++;
            this.edit();
        }
    }

    // Hiển thị hàng hoá cuối danh sách bảng
    private void last() {
        this.row = tblHangHoa.getRowCount() - 1;
        this.edit();
    }

    // Thêm mới loại hàng hoá
    private void insertLHH() {
        String tenLHH = MsgBox.prompt(this, "Nhập tên loại hàng hoá mới:");
        if (tenLHH != null && !tenLHH.isEmpty()) {
            LoaiHangHoa lhh = new LoaiHangHoa(tenLHH);
            try {
                lhhDAO.insert(lhh);
                this.fillToList();
                this.clearForm();
                tblModel.setRowCount(0);
                MsgBox.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại!");
                e.printStackTrace();
            }
        }
    }

    // Xoá loại hàng hoá
    private void deleteLHH() {
        if (lstLHH.isSelectionEmpty()) {
            MsgBox.alert(this, "Chưa chọn loại hàng hoá!");
        } else if (MsgBox.confirm(this, "Bạn chắc chắn muốn xoá loại hàng hoá này?")) {
            LoaiHangHoa lhh = lstLHH.getSelectedValue();
            try {
                lhhDAO.delete(lhh.getMaLHH());
                this.fillToList();
                this.clearForm();
                tblModel.setRowCount(0);
                MsgBox.alert(this, "Xoá thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xoá thất bại!");
                e.printStackTrace();
            }
        }
    }

    // Xác thực dữ liệu form
    private boolean isValidated() {
        if (txtMaHH.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập mã hàng hoá!");
            txtMaHH.requestFocus();
            return false;
        }
        if (txtTenHH.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập tên hàng hoá!");
            txtTenHH.requestFocus();
            return false;
        }
        if (txtDonGia.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập đơn giá!");
            txtDonGia.requestFocus();
            return false;
        }
        try {
            double donGia = Double.parseDouble(txtDonGia.getText());
            if (donGia < 0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            MsgBox.alert(this, "Đơn giá không hợp lệ!");
            txtDonGia.requestFocus();
            return false;
        }
        if (txtDonViTinh.getText().isEmpty()) {
            MsgBox.alert(this, "Chưa nhập đơn vị tính!");
            txtDonViTinh.requestFocus();
            return false;
        }
        return true;
    }

    // Thêm hàng hoá mới
    private void insert() {
        if (isValidated()) {
            return;
        }

        if (hhDAO.selectByID(txtMaHH.getText()) != null) {
            MsgBox.alert(this, "Mã hàng hoá đã tồn tại!");
            txtMaHH.requestFocus();
            return;
        }

        HangHoa hh = getForm();
        try {
            hhDAO.insert(hh);
            this.fillToTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
            e.printStackTrace();
        }
    }

    // Xoá hàng hoá
    private void delete() {
        if (MsgBox.confirm(this, "Bạn có chắc chắc muốn xoá hàng hoá này?")) {
            String maHH = (String) tblHangHoa.getValueAt(this.row, 0);
            try {
                hhDAO.delete(maHH);
                this.fillToTable();
                this.clearForm();
                MsgBox.alert(this, "Xoá thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xoá thất bại!");
                e.printStackTrace();
            }
        }
    }

    // Cập nhật hàng hoá
    private void update() {
        if (isValidated()) {
            HangHoa hh = getForm();
            try {
                hhDAO.update(hh);
                this.fillToTable();
                this.updateStatus();
                MsgBox.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại!");
                e.printStackTrace();
            }
        }
    }

    // Định dạng bảng
    private void formatTable() {
        String header[] = {"STT", "MÃ HÀNG HOÁ", "TÊN HÀNG HOÁ", "ĐƠN GIÁ", "ĐƠN VỊ TÍNH"};
        this.tblModel = new DefaultTableModel(header, 0) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

            @Override
            public Class getColumnClass(int columnIndex) {
                if (tblModel.getRowCount() == 0) {
                    return String.class;
                }
                if (getValueAt(0, columnIndex) == null) {
                    return Object.class;
                }
                return getValueAt(0, columnIndex).getClass();
            }
        };
        tblHangHoa.setModel(tblModel);

        tblHangHoa.removeColumn(tblHangHoa.getColumnModel().getColumn(1));
        // Set size column
        tblHangHoa.getColumnModel().getColumn(0).setPreferredWidth(40);
        tblHangHoa.getColumnModel().getColumn(1).setPreferredWidth(524);
        tblHangHoa.getColumnModel().getColumn(2).setPreferredWidth(125);
        tblHangHoa.getColumnModel().getColumn(3).setPreferredWidth(125);

        tblHangHoa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    // Đỗ lại dữ liệu 
